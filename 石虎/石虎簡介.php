                    <h2>
                    形態特徵：
                    </h2>
                     <p class="text-white-75 mb-5">體長50~65公分，尾長27~30公分，體重3~6公斤。體型似家貓但略大，俗稱山貓。身上花紋與花豹相似，故英文俗名又稱豹貓(leopard cat)。其頭部渾圓，軀幹圓長，額頭有2條灰白色縱帶，最大特徵是耳後有1塊微帶淡黃的白色圓斑，此2圓斑即為分類上之重要依據。體色為灰褐色，具黑褐色點狀斑，亦有金錢貓、華南豹貓之稱呼。尾部具有15~18個半環狀斑紋。夜視力佳、聽覺敏銳、犬齒發達。</p> 
                     <h2>
                     生物習性：
                     </h2>
                     <p class="text-white-75 mb-5">石虎狩獵工具是牠的兩隻前肢，骨骼粗壯有力，腳掌膨大。夜行性，白天於樹洞岩穴中休憩，於傍晚後始外出獵食，行動敏捷、身手靈活、善於爬樹。肉食性，會捕食魚類、鼠類、鳥類、蛙類及哺乳動物幼獸等。覓食採「守株待兔」的戰略，即沉穩守候在獵物必經的路線旁，遇獵物走過時，就躍出以前肢壓制。捕獲獵物時會以前腳拍擊壓制，或以嘴緊咬住，至死方休；不像其他小型貓科動物有捉放玩弄獵物的行為。</p>

                     <h2>
                     棲地分布：
                     <p class="text-white-75 mb-5">大多分布於海拔1,500以下之山區闊葉林中，常出現於半開發之農墾地附近，惟行蹤神秘，不易發現。目前分布於雪霸國家公園及太魯閣國家公園。</p>
                     
